#PARA IMPORTAR OUTRAS FUNÇÕES ARITMÉTICAS DEVEMOS IMPORTAR UMA BIBLIOTECA DO PYTHON

import math #comando para importar outras funções aritméticas, este comando é colocado no inicio do script;

print(math.floor(8.8)) #vai arredondar o numero para o numero inteiro mais baixo, no caso 8;
print(math.ceil(8.8)) #vai arredondar o numero para o numero inteiro mais alto, no caso 9;
print(math.sqrt(9)) #square root, é a raiz quadrada;